package Introduccion;

public class Variables {
    public static void main(String args[]){
        //Variables tipo string
        String nombre = "Lautaro";
        String apellido = "Tresca";

        //Variables tipo numeros
        //Enteros
        int edad = 22;
        int ano = 2023;

        //Decimales
        Double temperatura = 22.6;
        Float temperatura2 = 2.2f;

        System.out.println(nombre);
        System.out.println(apellido);
        System.out.println(edad + " años");
        System.out.println("año: " + ano);
        System.out.println(temperatura + " grados");
        System.out.println(temperatura2 + " grados");
    }
}
